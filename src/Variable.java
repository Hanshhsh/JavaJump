public class Variable {
    public static void main(String[] args) {

        int a = 1; // Number -> integer ...
        System.out.println(a);

        double b = 1.1;
        System.out.println(b);

        float c = 0L;
        System.out.print(c);

        String d = "Hello World";
        System.out.print(d);

    }
}
